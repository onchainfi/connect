/**
 * Get human-readable chain name from chain ID
 * @param chainId - EVM chain ID
 * @returns Formatted chain name or fallback "Chain {id}"
 */
export function getChainName(chainId: number): string {
  return CHAIN_ID_TO_NAME[chainId] || `Chain ${chainId}`;
}

/**
 * Check if a chain ID is known in our registry
 * @param chainId - EVM chain ID
 * @returns true if chain is in registry
 */
export function isKnownChain(chainId: number): boolean {
  return chainId in CHAIN_ID_TO_NAME;
}

